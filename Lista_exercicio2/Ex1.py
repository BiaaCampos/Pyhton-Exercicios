valor1 = int(input("Digite o valor 1: "))
valor2 = int(input("Digite o valor 2: "))
valor3 = int(input("Digite o valor 3: "))

valor1 = valor1
valor2 = valor2
valor3 = valor3
soma = valor1 + valor2 + valor3
subtracao = valor1 - valor2 - valor3
multiplicacao = valor1 * valor2 * valor3
divisao = valor1 / valor2 / valor3

print(f"valor da soma: {soma}")
print(f"valor da subtração: {subtracao}")
print(f"valor da multiplicação: {multiplicacao}")
print(f"valor da divisão: {divisao}")